        <h3>PHP</h3>
        <p>PHP (Hypertext Preprocessor) es un lenguaje de código abierto muy popular especialmente adecuado para el desarrollo web y que puede ser incrustado en HTML y combinado con otras tecnologias como javascript.</p>
        <p>Fue creado originalmente por Rasmus Lerdorf en el año 1995. Actualmente el lenguaje sigue siendo desarrollado con nuevas funciones por el grupo PHP.2​ encontrandose en la versión 7.2; Este lenguaje forma parte del software libre publicado bajo la licencia PHPv3_01, es una licencia de código abierto validada por Open Source Initiative y no tiene restricciones de "copyleft" asociadas con GPL (General Public License). </p>
        <p>PHP puede ser desplegado en la mayoría de servidores web y en casi todos los sistemas operativos y plataformas sin costo alguno. Actualmente PHP se encuentra instalado en más de 20 millones de sitios web y en un millón de servidores, pero el número de sitios basados en PHP se ha reducido progresivamente en los últimos años, por la aparición de nuevas tecnologías como Node.JS (Open Source), Golang (Lenguaje de Google), ASP.NET (Microsof), etc. </p>
      <p>PHP es un lenguaje similar a lenguajes más comunes de programación estructurada, como C y Perl, que permiten a la mayoría de los programadores crear aplicaciones complejas con una curva de aprendizaje poco pronunciada permiiendo involucrarse con aplicaciones de contenido dinámico sin tener que aprender todo un nuevo grupo de funciones.</p>
      <p>Una de la princiaples ventajas de al momento de usar PHP es su bajo costo de aprendizaje para un principiante, y a su vez ofrece una alta gama de características avanzadas para los programadores avanzados.</p>

